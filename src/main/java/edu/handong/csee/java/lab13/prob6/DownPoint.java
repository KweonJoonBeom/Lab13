package edu.handong.csee.java.lab13.prob6;

public class DownPoint {//DownPoint class
	private int x,y; //private int type vaiable x,y
	public DownPoint(int x,int y) { //constructor 
		this.x = x; //this class's x =x
		this.y = y; // this class's y =y 

	}
	public String toString() { //toString method 
		return "x: "+x+" y: "+y; //that return x,y information as lower case
	}
}
